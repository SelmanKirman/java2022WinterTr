package day04_dataCasting_increment;

public class C05_IncrementDecrement {

    public static void main(String[] args) {
        int sayi=20;

        System.out.println(sayi + 10);

        System.out.println(sayi);

        sayi=sayi+10;

        System.out.println(sayi);

        System.out.println(sayi=sayi+=10); // 40

        System.out.println(sayi);
        System.out.println(sayi+=10);
        System.out.println(sayi);

        // bir variable in degerini kalici olarak artirmak veya azalzmak isterseniy assignment sart







        System.out.println("30.satir" + sayi++);
        System.out.println("31.satir" + sayi);


        System.out.println("34.satir : " + sayi);
        System.out.println("35.satir : " + sayi);
    }



}
